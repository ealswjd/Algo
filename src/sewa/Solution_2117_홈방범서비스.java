package sewa;

import java.io.BufferedReader;
import java.io.InputStreamReader;
import java.util.StringTokenizer;

// 손해를 보지 않으면서 홈방범 서비스를 가장 많은 집들에 제공하는 서비스 영역을 찾고,
// 그 때의 홈방범 서비스를 제공 받는 집들의 수를 출력하는 프로그램을 작성하라.
public class Solution_2117_홈방범서비스 {
	static int N, M, max;
	static int[][] map, visited;

	public static void main(String[] args) throws Exception {
		BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
		int T = Integer.parseInt(br.readLine());
		StringTokenizer st;
		
		StringBuilder sb = new StringBuilder();
		for(int tc=1; tc<=T; tc++) {
			sb.append("#"+tc+" ");
			st = new StringTokenizer(br.readLine());
			N = Integer.parseInt(st.nextToken());
			M = Integer.parseInt(st.nextToken());
			
			map = new int[N][N];
			for(int i=0; i<N; i++) {
				st = new StringTokenizer(br.readLine());
				for(int j=0; j<N; j++) {
					map[i][j] = Integer.parseInt(st.nextToken());
				}
			}//for
			
			max = 0;
			for(int i=0; i<N; i++) {
				for(int j=0; j<N; j++) {
					service(i, j);					
				}
			}
			sb.append(max);
			sb.append("\n");
		}//for tc

		System.out.print(sb);
	}//main
	
	private static void service(int x, int y) {
		
		int cnt = 0;
		for(int k=1; k<=N+1; k++) {
			cnt = 0;
			visited = new int[N][N];
			for(int i=x-k; i<=x+k; i++) {
				for(int j=y-k; j<=y+k; j++) {
					if(i<0 || i>=N || j<0 || j>=N) continue;
					if(Math.abs(x-i) + Math.abs(y-j) < k) {
						if(visited[i][j] == 1) continue;
						cnt += map[i][j];
						visited[i][j] = 1;
					}
				}
			}//for i
			
			// 운영 비용 = K * K + (K - 1) * (K - 1)
			// 보안회사의 이익(4) = 서비스 제공받는 집들을 통해 얻는 수익(3*3) - 운영 비용(5)
			int cost =  k*k + (k-1) * (k-1);
			if(cost <= (cnt*M)) {
				max = Math.max(max, cnt);
			}
		}//for k
		
	}//service



}//class

